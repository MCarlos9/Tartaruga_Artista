import turtle

turtle = turtle.Turtle()
turtle.shape('turtle')


turtle.forward(100) #Linhas reduzidas  #Comprimento alterado 
turtle.right(90) #Direção alterada
turtle.forward(150) #Linhas reduzidas  #Comprimento alterado 100-->150   #Formato Retangular
turtle.right(90) #Direção alterada
turtle.forward(100) #Linhas reduzidas  #Comprimento alterado
turtle.right(90) #Direção alterada
turtle.forward(150) #Linhas reduzidas  #Comprimento alterado 100-->150   #Formato Retangular
turtle.right(90) #Direção alterada